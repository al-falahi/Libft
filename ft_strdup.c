#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	int		i;
	int		len;

	len = ft_strlen(s);
	dst = (char *) malloc(sizeof(char) * (len +1));
	if (dst == NULL)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
